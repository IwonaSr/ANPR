public class MaxGap {

    int index;
    double max;

    public MaxGap(){
        this.index = index;
        this.max = max;
    }
    public MaxGap(int index, double max){
        this.index = index;
        this.max = max;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public double getMax() {
        return max;
    }

    public void setMax(double max) {
        this.max = max;
    }

}
